#Etape 02 : les types de données
print(type(20)) # entier
print(type(3.14)) # flottant
print(type('bonjour')) # chaine de caractères, les string
print(type(True)) # bouleen
print(type([1,2,3,4,5])) # les listes
print(type((1,3,5,6,4))) # les tuple non modifiables
print(type({'nom':'sanogo', 'prenom':'madou'})) # les dictionnaires
print(type({3.14, 2.71})) # les set, valeur unique et non ordonnée
print(type((1 + 1j))) # les nombres complexes